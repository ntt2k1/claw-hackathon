export function calculateDNA(answers: { questionNum: number; selectedOption: string }[]) {
  const rawScores: Record<string, number> = {
    "Ẩm thực": 0, "Văn hoá": 0, "Thiên nhiên": 0, "Phiêu lưu": 0,
    "Sang chảnh": 0, "Giao lưu": 0, "Tọa độ ngách": 0, "Thư giãn": 0,
    "Nhiếp ảnh": 0, "Hiệu quả": 0
  };

  answers.forEach(({ questionNum, selectedOption }) => {
    const q = QUESTIONS.find(q => q.num === questionNum);
    const opt = q?.options.find(o => o.letter === selectedOption);
    if (!opt) return;
    Object.entries(opt.scores).forEach(([axis, delta]) => {
      rawScores[axis] = (rawScores[axis] || 0) + delta;
      if (rawScores[axis] < 0) rawScores[axis] = 0;
    });
  });

  const normalized: Record<string, number> = {};
  Object.keys(rawScores).forEach(axis => {
    const max = MAX_POSSIBLE[axis] || 1;
    normalized[axis] = Math.min(100, Math.round((rawScores[axis] / max) * 100));
  });

  return { rawScores, normalized };
}

export function getTop3(normalized: Record<string, number>) {
  return Object.entries(normalized)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([axis, score]) => ({ axis, score }));
}

export function lookupPersona(top3Axes: string[], normalizedDNA: Record<string, number> = {}) {
  const top3Set = new Set(top3Axes);
  let bestMatch: any = null;
  let maxOverlap = 0;
  let maxScore = -1;

  for (const p of PERSONA_MAP) {
    if (p.key === "default") continue;

    const pKeys = p.key as string[];
    let overlap = 0;
    let score = 0;

    for (const k of pKeys) {
      if (top3Set.has(k)) overlap++;
      score += normalizedDNA[k] || 0;
    }

    // Special rule for Adventure Nomad (comfort < 20)
    if (p.name === "Adventure Nomad" && (normalizedDNA["Thư giãn"] || 50) >= 20) {
      continue;
    }

    if (overlap > maxOverlap || (overlap === maxOverlap && score > maxScore)) {
      maxOverlap = overlap;
      maxScore = score;
      bestMatch = p;
    }
  }

  // Fallback only if no overlap at all
  if (maxOverlap === 0) {
    return PERSONA_MAP.find(p => p.key === "default");
  }

  return bestMatch || PERSONA_MAP.find(p => p.key === "default");
}

